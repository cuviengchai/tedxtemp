          <span>หลังจากเดินมาตลอดบ่ายไปในหลายๆ ที่ที่คุณไพลินได้ชวนเราไปชมชีวิตและวิถีความเป็นเจริญกรุง เราก็กลับมาที่จุดสุดท้ายซึ่งเป็นโฟกัสของ Talk ในปีที่แล้วนั่นคือ ร้านนาฬิกา หว่าซันวอช </span>

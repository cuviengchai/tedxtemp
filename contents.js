          <span>ระหว่างสนทนาเราเองก็นึกตามไปว่าในขณะที่ยุคสมัยที่เราสามารถรู้เวลา จากอุปกรณ์มากมายจนหลายคนอาจจะไม่ค่อยได้ใช้นาฬิกาแล้วด้วยซ้ำ ทำไมถึงยังมีคนอยากเอานาฬิกาเก่ามาซ่อม บางครั้งเราอาจจะไม่ได้ต้องการรู้หรอกว่าเวลาปัจจุบันเป็นกี่โมงกี่ยามเพียงแต่อยากจะให้ช่วงเวลาที่นาฬิกาเหล่านั้นได้เดินและเก็บสะสมไว้ ยังคงเดินอยู่ต่อไปไม่ตายหายไปจากเราพร้อมกับกลไกอันโบราณและเก่าแก่ของมันก็เป็นได้</span>
